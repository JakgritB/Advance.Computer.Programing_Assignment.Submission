package boottapak.jakgrit.lab2;

public class PrimitiveDataType {
    public static void main(String[] args){
        
        String StudentID = "663040111-9";
        String FisrName = "Jakgrit";
        Byte ByteValue = 19;
        Short ShortValue = 119;
        int intValue = 401119;
        long longValue = 30401119;
        float floatValue = 0.119f;
        double doubleValue = 0.30401119;
        char charValue = 'J';
        boolean booValue = true;

        System.out.println("Student ID : " + StudentID);
        System.out.println("First Name : " + FisrName);
        System.out.println("Byte Value: " + ByteValue);
        System.out.println("Short Value:" + ShortValue);
        System.out.println("Int Value: " + intValue);
        System.out.println("Long Value: " + longValue);
        System.out.println("Float Value: " + floatValue);
        System.out.println("Double Value: " + doubleValue);
        System.out.println("Char Value: " + charValue);
        System.out.println("Boolean Value: " + booValue);

    }
    
}
